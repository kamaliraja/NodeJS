 //In this program using for loops to display sum of natural number

let sum = 0;
const n = 6;

// looping from i = 1 to n
// in each iteration, i is increased by 1

for (let i = 1; i <= n; i++) {
    sum += i;                    // sum = sum + i
}

console.log('Sum of Natural numbers using for loop :', sum);  // prints 21




